#Take 2 inputs from the user as a integers, check whether both are equal or not (if-else)

a=int(input("Write a number:"))
b=input(("Write another number:"))

if (a == b):
    print(f'The first input {a} and the second input {b} are equal')
else:
    print(f'The first input {a} and the second input {b} are not equal')
